    private void add() {
        sanPham spPham = new sanPham();
        spPham.setTen(txtten.getText());
        spPham.setNhaCungCap(txtnhacungcap.getText());
        spPham.setDonvi(jComboBox1.getSelectedItem().toString());
        spPham.setMa(Double.parseDouble(txtma.getText()));
        spPham.setGia(Double.parseDouble(txtdongia.getText()));

        sp.add(spPham);
    }

    public void fiiltable() {
        tableModel.setRowCount(0);
        for (sanPham i : sp) {
            Object rowdata[] = new Object[5];
            rowdata[0] = i.getMa();
            rowdata[1] = i.getTen();
            rowdata[2] = i.getDonvi();
            rowdata[3] = i.getGia();
            rowdata[4] = i.getNhaCungCap();
            tableModel.addRow(rowdata);
        }
        tableModel.fireTableDataChanged();
    }

