    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtten.getText().equals("")) {
            sb.append("tên không được để trống!!!\n");
        }
        if (txtma.getText().equals("")) {
            sb.append("mã không được để trống!!!");
        }
        if (txtdongia.getText().equals("")) {
            sb.append("đơn giá không được để trống!!!");
        }
        if (txtnhacungcap.getText().equals("")) {
            sb.append("nhà cung cấp không được để trống!!!");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Xác nhận lại", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }

        for (sanPham aPham : sp) {
            if (aPham.getTen().equals(txtten.getText())) {
                sp.remove(aPham);
                break;
            }
        }
        fiiltable();
        btnchinhsuaActionPerformed(evt);//xoá trắng hết
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnchinhsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchinhsuaActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtten.getText().equals("")) {
            sb.append("tên không được để trống!!!\n");
        }
        if (txtma.getText().equals("")) {
            sb.append("mã không được để trống!!!");
        }
        if (txtdongia.getText().equals("")) {
            sb.append("đơn giá không được để trống!!!");
        }
        if (txtnhacungcap.getText().equals("")) {
            sb.append("nhà cung cấp không được để trống!!!");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn cập nhật không?", "Xác nhận lại", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            for (sanPham aPham : sp) {
                if (aPham.getTen().equals(txtten.getText())) {
                    aPham.setTen((txtten.getText()));
                    aPham.setMa(Float.parseFloat(txtma.getText()));
                    aPham.setDonvi(jComboBox1.getSelectedItem().toString());
                }
            }
            fiiltable();
//        btnchinhsuaActionPerformed(evt);
        }


    }//GEN-LAST:event_btnchinhsuaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int D = jTable1.getSelectedRow();
        check(D);
    }//GEN-LAST:event_jTable1MouseClicked
    public void check(int D) {
        txtten.setText(sp.get(D).getTen());
        txtma.setText(String.valueOf(sp.get(D).getMa()));
        txtdongia.setText(sp.get(D).getDonvi());
        txtnhacungcap.setText(String.valueOf(sp.get(D).getNhaCungCap()));
        jTable1.setRowSelectionInterval(D, D);

    }

